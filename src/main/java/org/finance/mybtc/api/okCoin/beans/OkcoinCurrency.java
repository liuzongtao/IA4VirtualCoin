package org.finance.mybtc.api.okCoin.beans;

/**
 * @author zongtao liu
 *
 */
public class OkcoinCurrency {

	private float cny;
	private float btc;
	private float ltc;
	private float eth;

	/**
	 * @return the cny
	 */
	public float getCny() {
		return cny;
	}

	/**
	 * @param cny
	 *            the cny to set
	 */
	public void setCny(float cny) {
		this.cny = cny;
	}

	/**
	 * @return the btc
	 */
	public float getBtc() {
		return btc;
	}

	/**
	 * @param btc
	 *            the btc to set
	 */
	public void setBtc(float btc) {
		this.btc = btc;
	}

	/**
	 * @return the ltc
	 */
	public float getLtc() {
		return ltc;
	}

	/**
	 * @param ltc
	 *            the ltc to set
	 */
	public void setLtc(float ltc) {
		this.ltc = ltc;
	}

	/**
	 * @return the eth
	 */
	public float getEth() {
		return eth;
	}

	/**
	 * @param eth
	 *            the eth to set
	 */
	public void setEth(float eth) {
		this.eth = eth;
	}

}
