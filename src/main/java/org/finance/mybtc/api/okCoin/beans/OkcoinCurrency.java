package org.finance.mybtc.api.okCoin.beans;

/**
 * @author zongtao liu
 *
 */
public class OkcoinCurrency {

	private double cny;
	private double btc;
	private double ltc;
	private double eth;

	/**
	 * @return the cny
	 */
	public double getCny() {
		return cny;
	}

	/**
	 * @param cny
	 *            the cny to set
	 */
	public void setCny(double cny) {
		this.cny = cny;
	}

	/**
	 * @return the btc
	 */
	public double getBtc() {
		return btc;
	}

	/**
	 * @param btc
	 *            the btc to set
	 */
	public void setBtc(double btc) {
		this.btc = btc;
	}

	/**
	 * @return the ltc
	 */
	public double getLtc() {
		return ltc;
	}

	/**
	 * @param ltc
	 *            the ltc to set
	 */
	public void setLtc(double ltc) {
		this.ltc = ltc;
	}

	/**
	 * @return the eth
	 */
	public double getEth() {
		return eth;
	}

	/**
	 * @param eth
	 *            the eth to set
	 */
	public void setEth(double eth) {
		this.eth = eth;
	}

}
