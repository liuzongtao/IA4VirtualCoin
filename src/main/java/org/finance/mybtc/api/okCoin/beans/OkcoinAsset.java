public class OkcoinAsset {
	
	private float net;
	private float total;
	/**
	 * @return the net
	 */
	public float getNet() {
		return net;
	}
	/**
	 * @param net the net to set
	 */
	public void setNet(float net) {
		this.net = net;
	}
	/**
	 * @return the total
	 */
	public float getTotal() {
		return total;
	}
	/**
	 * @param total the total to set
	 */
	public void setTotal(float total) {
		this.total = total;
	}
	
	
}
